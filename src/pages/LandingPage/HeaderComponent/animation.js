var duration = 100;
//1
export const animation1 = () => {
    function randomNum1(){
    let test = document.getElementById('1');
    test.textContent= `${Math.floor(Math.random() * (9 - 0)) + 0}`;
}
function endPoint1(){
    let test = document.getElementById('1');
    test.textContent='F' ;
}

for (let i=0; i<16; i++){
    if(i<15) {
setTimeout(randomNum1, i*duration);
    } else {
setTimeout(endPoint1, i*duration);
    }
}
}

